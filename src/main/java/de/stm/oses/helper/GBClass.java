package de.stm.oses.helper;

public class GBClass {

	private int id;
    private String gb;

    public GBClass(int id, String gb) {
        this.id = id;
        this.gb = gb;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getGB() {
        return gb;
    }

    public void setGB(String gb) {
        this.gb = gb;
    }
}